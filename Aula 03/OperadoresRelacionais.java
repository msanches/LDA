public class OperadoresRelacionais {
    public static void main(String[] args) {
        boolean maior, menor, igual;
        int a = 100; 
        int b = 333;
        maior = a > b;
        menor = a < b;
        igual = a == b;
        System.out.println("a > b: " + maior);
        System.out.println("a < b: " + menor);
        System.out.println("a == b: " + igual);
    }
    
}